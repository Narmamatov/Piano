let dO = document.querySelector(".do");
window.addEventListener("keydown", (e) => {
  if (e.key === "z") {
    let nDo = new Audio();
    nDo.src = "./sounds/do.mp3";
    nDo.play();
    dO.style.borderBottom = "15px solid rgb(62, 61, 61)";
    dO.style.borderRight = "5px solid rgb(62, 61, 61)";
    dO.style.background = "linear-gradient(to top, #616060, #ffffff)";

    setTimeout(() => {
      dO.style.background = "white";
      dO.style.borderBottom = "25px solid rgb(62, 61, 61)";
      dO.style.borderRight = "15px solid rgb(62, 61, 61)";
      dO.style.background = "linear-gradient(to top, #d1d1d1, #ffffff)";
    }, 100);
  }
});
